        <h2 class="section-title">Taylor Ham or Pork Roll?</h2>
      </div>
      <div style="background: var(--bb-white); border-radius: var(--radius-lg); padding: var(--space-xl); box-shadow: var(--shadow-md);">
        <p style="font-size: 1.05rem; line-height: 1.8;">If you're from North Jersey, it's <strong>Taylor Ham</strong>. If you're from South Jersey, it's <strong>Pork Roll</strong>. The dividing line runs roughly along Route 78 and the Union-Middlesex county border.</p>
        <p style="font-size: 1.05rem; line-height: 1.8;">The product itself dates back to 1856, when John Taylor of Trenton created "Taylor's Prepared Ham." In 1906, the Pure Food and Drug Act forced a name change since it technically wasn't ham — hence "pork roll." But North Jersey never got the memo.</p>
        <p style="font-size: 1.05rem; line-height: 1.8;">Here at Bagel Boyz in Hazlet? We're right on the line. We respect both sides of the debate. <strong>But we'll make it however you order it — just don't forget the SPK.</strong></p>
        <p style="font-size: 1.05rem; line-height: 1.8; margin-bottom: 0;"><em>SPK = Salt, Pepper, Ketchup. If you have to ask, you're probably not from Jersey. But that's okay — we'll show you the way.</em></p>
